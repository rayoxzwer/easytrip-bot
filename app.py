# SOLIDIFIED MULTI-STAGE SYSTEM INSTRUCTIONS
# =====================================================================
SYSTEM_INSTRUCTION = """
You are an empathetic, natural, and efficient travel intake assistant for EasyTrip agency.

STAGE 1: GATHERING INFORMATION
Ask 1-2 short questions at a time to gather exactly these 5 elements:
1. Destination
2. Budget boundaries
3. Duration of stay
4. Vibe or specific interests
5. Dietary preferences or restrictions (accept answers like "none", "no restrictions", or "no")

STAGE 2: PRESENTING THE SUMMARY
The absolute second you have collected all 5 pieces of information, do not ask more follow up questions. Immediately present a clean text-based review summary titled exactly: "✨ **Final Plan Review**".
List out all 5 gathered details cleanly using bullet points. Conclude the message by asking: "Do you want to make any changes? If it looks good, let me know and I will build your handmap plan PDF file!"

STAGE 3: THE PDF GENERATION TRIGGER
ONLY after you have explicitly displayed the "✨ **Final Plan Review**" text block in a previous turn, if the user confirms everything looks good (e.g., says "yes", "looks great", "no changes", "finalize"): Output the exact string "[TRIGGER_PDF]" along with a friendly closing sign-off. Do not output raw python or code snippets.
"""

# =====================================================================
# TELEGRAM DISPATCH HANDLERS
# =====================================================================
@dp.message(CommandStart())
async def cmd_start(message: types.Message):
    user_id = message.from_user.id
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute("DELETE FROM chat_history WHERE user_id = ?", (user_id,))
    conn.commit()
    conn.close()
    
    welcome = "👋 Welcome to EasyTrip! I'm your AI partner. Where are we traveling next?"
    # FIX: We do NOT save the welcome message to chat_history anymore.
    # This guarantees the payload sequence sent to Gemini strictly starts with a 'user' turn.
    await message.answer(welcome)

@dp.message()
async def handle_chat_turn(message: types.Message):
    user_id = message.from_user.id
    if not message.text:
        return

    save_message(user_id, "user", message.text)
    history_rows = get_history(user_id)
    
    contents_payload = []
    for row in history_rows:
        contents_payload.append(
            genai_types.Content(role=row["role"], parts=[genai_types.Part.from_text(text=row["parts"][0])])
        )

    try:
        response = await ai_client.aio.models.generate_content(
            model="gemini-2.0-flash",
            contents=contents_payload,
            config=genai_types.GenerateContentConfig(
                system_instruction=SYSTEM_INSTRUCTION,
                max_output_tokens=700,
                temperature=0.3
            )
        )
        
        ai_response_text = response.text if response.text else ""

        if "[TRIGGER_PDF]" in ai_response_text:
            clean_reply = ai_response_text.replace("[TRIGGER_PDF]", "").strip()
            if clean_reply:
                await message.answer(clean_reply)
            
            await message.answer("⚙️ *Compiling your Handmap PDF Plan... Please wait a brief moment.*", parse_mode="Markdown")
            
            pdf_data = create_itinerary_pdf(history_rows)
            input_file = BufferedInputFile(pdf_data.read(), filename="EasyTrip_Itinerary.pdf")
            
            await message.answer_document(document=input_file, caption="✈️ Ready! Here is your official EasyTrip travel plan document. Have an amazing trip!")
            
            # Flush history clean for their next session
            conn = sqlite3.connect(DB_FILE)
            cursor = conn.cursor()
            cursor.execute("DELETE FROM chat_history WHERE user_id = ?", (user_id,))
            conn.commit()
            conn.close()
        else:
            if ai_response_text:
                save_message(user_id, "model", ai_response_text)
                await message.answer(ai_response_text)
            else:
                # Upgraded fallback warning for clearer diagnostics
                logging.warning(f"Empty text token generated for user: {user_id}")
                await message.answer("Let's make sure I have that down correctly. Could you repeat your last response?")

    except Exception as e:
        logging.error(f"Engine Exception: {e}")
        await message.answer(f"❌ Debug Error: {str(e)}")

# =====================================================================
# LIFECYCLE MANAGEMENT & WEB SYSTEM
# =====================================================================
async def run_bot_resiliently():
    while True:
        try:
            logging.info("Launching aiogram polling engine...")
            await dp.start_polling(bot, handle_signals=False, polling_timeout=30)
        except asyncio.CancelledError:
            break
        except Exception as proxy_error:
            logging.exception("CRITICAL Polling loop failure!")
            await asyncio.sleep(5)

@asynccontextmanager
async def lifespan(fastapi_app: FastAPI):
    init_db()
    supervisor_task = asyncio.create_task(run_bot_resiliently())
    active_background_tasks.add(supervisor_task)
    supervisor_task.add_done_callback(active_background_tasks.discard)
    yield
    await dp.stop_polling()
    supervisor_task.cancel()

app = FastAPI(lifespan=lifespan)

@app.get("/")
def health_check():
    return {"status": "EasyTrip Core Engine is online 24/7"}
