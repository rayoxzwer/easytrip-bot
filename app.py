import os
import sys
import json
import io
import asyncio
import sqlite3
import logging
from contextlib import asynccontextmanager
from fastapi import FastAPI
from aiogram import Bot, Dispatcher, types
from aiogram.filters import CommandStart
from aiogram.types import BufferedInputFile
from google import genai
from google.genai import types as genai_types

from reportlab.lib.pagesizes import letter
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle

# Setup Logging Metrics
logging.basicConfig(level=logging.INFO, format="%(asctime)s - %(levelname)s - %(message)s")

TELEGRAM_TOKEN = os.getenv("TELEGRAM_TOKEN")
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")

if not TELEGRAM_TOKEN or not GEMINI_API_KEY:
    logging.critical("Deployment failed: Environment variables are missing!")
    sys.exit(1)

bot = Bot(token=TELEGRAM_TOKEN)
dp = Dispatcher()
ai_client = genai.Client(api_key=GEMINI_API_KEY)
DB_FILE = "easy_trip.db"

active_background_tasks = set()

# =====================================================================
# CORE ENGINE DATABASE LOGIC
# =====================================================================
def init_db():
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute('CREATE TABLE IF NOT EXISTS chat_history (user_id INTEGER, role TEXT, content TEXT)')
    conn.commit()
    conn.close()

def get_history(user_id: int):
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute("SELECT role, content FROM chat_history WHERE user_id = ?", (user_id,))
    rows = cursor.fetchall()
    conn.close()
    return [{"role": r, "parts": [c]} for r, c in rows]

def save_message(user_id: int, role: str, content: str):
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute("INSERT INTO chat_history (user_id, role, content) VALUES (?, ?, ?)", (user_id, role, content))
    conn.commit()
    conn.close()

# =====================================================================
# DYNAMIC PDF GENERATOR SERVICE
# =====================================================================
def create_itinerary_pdf(chat_history: list) -> io.BytesIO:
    pdf_buffer = io.BytesIO()
    doc = SimpleDocTemplate(pdf_buffer, pagesize=letter, rightMargin=40, leftMargin=40, topMargin=40, bottomMargin=40)
    story = []
    
    styles = getSampleStyleSheet()
    title_style = ParagraphStyle('TitleStyle', parent=styles['Heading1'], fontSize=24, spaceAfter=20)
    body_style = ParagraphStyle('BodyStyle', parent=styles['Normal'], fontSize=12, leading=16, spaceAfter=10)
    
    story.append(Paragraph("✈️ EasyTrip — Your Custom Handmap Plan", title_style))
    story.append(Spacer(1, 15))
    story.append(Paragraph("Here is the verified scope generated by your AI travel companion:", styles['Heading2']))
    story.append(Spacer(1, 10))
    
    for turn in chat_history:
        if turn['role'] == 'model' and "Final Plan Review" in turn['parts'][0]:
            clean_text = turn['parts'][0].replace("[TRIGGER_PDF]", "").replace("**", "")
            for line in clean_text.split('\n'):
                if line.strip():
                    story.append(Paragraph(line, body_style))
            break
    else:
        story.append(Paragraph("Custom trip data finalized successfully. Secure packing and clear skies ahead!", body_style))
        
    doc.build(story)
    pdf_buffer.seek(0)
    return pdf_buffer

